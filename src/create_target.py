import pandas as pd

# =============================================================================
# CONFIG
# =============================================================================

INPUT_FILE = "data/processed/feature_dataset_v3.csv"
OUTPUT_FILE = "data/processed/model_dataset.csv"

# =============================================================================
# LOAD
# =============================================================================

print("\n================================================================================")
print("CREATING TARGET")
print("================================================================================")

df = pd.read_csv(INPUT_FILE)

df["date"] = pd.to_datetime(df["date"])

df = (
    df
    .sort_values(["player", "date"])
    .reset_index(drop=True)
)

# =============================================================================
# TARGET
# =============================================================================

print("\n[GENERANDO TARGET]")

df["target_distance_next_day"] = (
    df
    .groupby("player")["distance_m"]
    .shift(-1)
)

# =============================================================================
# REMOVE LAST OBSERVATION OF EACH PLAYER
# =============================================================================

before = len(df)

df = df.dropna(subset=["target_distance_next_day"])

after = len(df)

print(f"\nFilas eliminadas: {before - after}")

# =============================================================================
# SAVE
# =============================================================================

df.to_csv(OUTPUT_FILE, index=False)

print("\n================================================================================")
print("TARGET GENERADO")
print("================================================================================")

print(f"Filas finales: {len(df):,}")
print(f"Archivo: {OUTPUT_FILE}")